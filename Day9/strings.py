name = "Virat"

# Print the first character of a string.
print(name[0])

# Print the last character using negative indexing.
print(name[-1])

# Print the first 5 characters using slicing.
print(name[0:6])

# Print the last 3 characters using negative slicing.
print(name[-3:])

# Reverse the string using [::-1].
print(name[ ::-1])