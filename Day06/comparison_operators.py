# Equal To (==)
print(10==8)    # "==" is used to check the values are same or not 
print(10==10)

# Not Equal to (!=) 
print(10 != 10)  
print(14 != 6)

# Greater Than (>)
print(6>2)
print(15>20)

#less than (<)
print(4<5)
print(65<89)

#less than equal to (<=)
print(10<=12)
print(89<=24)

#greater than equal to (>=)
print(23>=13)
print(56>=99)